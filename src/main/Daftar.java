    private void BSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSimpanActionPerformed
        // TODO add your handling code here:
        String alert = "";
        int numAlert = 0;
        if (IIdUser.getText().equals("")) {
            alert = alert+"&koma; Id User";
            numAlert = numAlert+1;
        }
        if (CLevel.getSelectedItem().equals("--Select--")) {
            alert = alert+"&koma; Level";
            numAlert = numAlert+1;
        }
        if (INama.getText().equals("")) {
            alert = alert+"&koma; Nama User";
            numAlert = numAlert+1;
        }
        if (!JkL.isSelected() && !JkP.isSelected()) {
            alert = alert+"&koma; Jenis Kelamin";
            numAlert = numAlert+1;
        }
        if (INope.getText().equals("")) {
            alert = alert+"&koma; Nomor Hp";
            numAlert = numAlert+1;
        }
        if (IUsername.getText().equals("")) {
            alert = alert+"&koma; Username";
            numAlert = numAlert+1;
        }
        if (String.valueOf(IPassword.getPassword()).equals("")) {
            alert = alert+"&koma; Password";
            numAlert = numAlert+1;
        }
        if (alert != "") {
            if (numAlert > 1) {
                alert=alert.replace("&koma;",",");
                alert=alert.replaceFirst(",", "");
            } else {
                alert=alert.replace("&koma;","");
            }
            JOptionPane.showMessageDialog(null, alert+" Belum diisi!!");
        } else {
            int Confirm = JOptionPane.showOptionDialog(this, "Ingin simpan data?", "Konfirmasi", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
            if(Confirm == JOptionPane.YES_OPTION){
                input_data();
                login login = new login();
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Akun anda Sudah didaftarkan !!");
                login.setVisible(true);
            }
        }
    }//GEN-LAST:event_BSimpanActionPerformed

