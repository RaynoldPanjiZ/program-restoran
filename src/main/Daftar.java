    private void IDOtomatis() {
        try {
            st=koneksi.con2.createStatement();
            String sql="SELECT * FROM tbl_karyawan order by ID_KARYAWAN desc";
            rs=st.executeQuery(sql);
            if(rs.next()) {
                String ID=rs.getString("ID_KARYAWAN").substring(4);
                String NO = "" + (Integer.parseInt(ID) + 1);
                String Nol = "";
                if(NO.length()==1) {
                    Nol="000";
                } else if(NO.length()==2) {
                    Nol="00";
                }
                IIdUser.setText("USER" + Nol + NO);
            } else {
                IIdUser.setText("USER0001");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    //load Data Level Daroi Tabel Level ke ComboBox Level
    public void level() {
        try {
            st = koneksi.con2.createStatement();
            String query = "SELECT * FROM tbl_level";
            rs = st.executeQuery(query);
            while (rs.next()) {
                CLevel.addItem(rs.getString("LEVEL"));
            }
            rs.close();
        } catch (SQLException e)  { }
    }

    public void input_data() {
        String jk="";
        try {
            //Mengambil Data Jenis Kelamin
            if(JkL.isSelected()) {
                jk = JkL.getText();
            } else {
                jk = JkP.getText();
            }

            //Mengambil ComboBox
            st=koneksi.con2.createStatement();
            String query = "SELECT ID_LEVEL FROM tbl_level WHERE LEVEL='"+CLevel.getSelectedItem()+"'";
            rs = st.executeQuery(query);
            String id = "";
            if(rs.next()) {
                id = rs.getString("ID_LEVEL");
            }
            //Query Input ke Tabel USer
            String sql="INSERT INTO tbl_karyawan values('"+IIdUser.getText()
            +"','"+id
            +"','"+INama.getText()
            +"','"+jk
            +"','"+INope.getText()
            +"','"+IUsername.getText()
            +"','"+IPassword.getText()
            +"')";
            st.execute(sql);
            JOptionPane.showMessageDialog(null, "Data Barang Berhasil Dimasukan");
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

